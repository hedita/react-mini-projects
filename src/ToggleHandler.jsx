import React, { useState } from "react";

export const ToggleHandler0 = () => {
  const [toggle, setToggle] = useState("off");

  const handleClick = () => {
    setToggle(!toggle);
  };
  return (
    <div
      onClick={handleClick}
      style={{
        width: 50,
        height: 50,
        background: toggle ? "red" : "blue",
      }}
    >
    </div>
  );
};
